#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	src_len;
	size_t	dest_len;

	src_len = ft_strlen(src);
	dest_len = 0;
	while (dest_len < size && dest[dest_len])
		dest_len++;
	i = 0;
	if (size <= dest_len)
		return (src_len + size);
	while ((i < (size - dest_len - 1)) && src[i])
	{
		dest[dest_len + i] = src[i];
		i++;
	}
	dest[dest_len + i] = '\0';
	return (dest_len + src_len);
}
